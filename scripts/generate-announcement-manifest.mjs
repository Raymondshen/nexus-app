// Run: node scripts/generate-announcement-manifest.mjs
// Regenerates src/features/profile/screens/announcementImageManifest.generated.ts
// from whatever image files are present under public/img/announcements/ —
// keeps the Image Source picker (DeveloperUserAnnouncements.tsx) in sync
// with that folder without a hand-edited array. Wired into `predev`/`prebuild`
// (package.json) and the Vercel buildCommand (vercel.json) so it runs on
// every local dev start, `npm run build`, and deploy.

import { readdirSync, writeFileSync } from 'fs'
import { join, dirname, extname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const IMAGE_DIR = join(__dirname, '..', 'public', 'img', 'announcements')
const OUT_FILE  = join(__dirname, '..', 'src', 'features', 'profile', 'screens', 'announcementImageManifest.generated.ts')

const IMAGE_EXTENSIONS = new Set(['.svg', '.png', '.jpg', '.jpeg', '.webp', '.gif'])

const filenames = readdirSync(IMAGE_DIR)
  .filter((f) => IMAGE_EXTENSIONS.has(extname(f).toLowerCase()))
  .sort((a, b) => a.localeCompare(b))

const options = filenames
  .map((f) => `  { filename: ${JSON.stringify(f)}, path: ${JSON.stringify(`/img/announcements/${f}`)} },`)
  .join('\n')

const contents = `// AUTO-GENERATED by scripts/generate-announcement-manifest.mjs — do not edit by hand.
// Regenerated from public/img/announcements/ on every dev start / build (see
// predev/prebuild in package.json and buildCommand in vercel.json). Adding a
// new banner image is now just: drop the file in that folder and commit —
// no manual manifest edit needed.

export interface AnnouncementImageOption {
  filename: string
  path:     string
}

export const ANNOUNCEMENT_IMAGE_OPTIONS: AnnouncementImageOption[] = [
${options}
]
`

writeFileSync(OUT_FILE, contents)
console.log(`Wrote ${filenames.length} announcement image option(s) to ${OUT_FILE}`)
